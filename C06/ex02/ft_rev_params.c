#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	n;
	int	i;

	n = argc - 1;
	while (n > 0)
	{
		i = 0;
		while (argv[n][i])
		{
			write(1, &argv[n][i], 1);
			i++;
		}
		n--;
		write(1, "\n", 1);
	}
	return (0);
}
